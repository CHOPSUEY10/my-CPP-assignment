#include <iostream>
using namespace std;

int main(){

    // OPERATORS 
    // digunakan untuk melakukan operasi aritmatika pada variabel dan nilai 

    // pada contoh dibawah, kita menggunakan operator + untuk menambah dua nilai 

    int x = 90 + 90;
    cout << x ;
     

    // operator lainnya 

    int y = 100 - 90;
    cout << y << '\n';

    int z = 100 / 20;
    cout << z << '\n';

    int a = 100 * 3;
    cout << a << '\n';

    int b = 100 % 3;
    cout << b << '\n';

    int c = 100;
    ++c;
    cout << c << '\n';

    int d = 100;
    --d;
    cout << d << '\n';

    return 0;




}