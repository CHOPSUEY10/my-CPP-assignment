# include <iostream>
using namespace std; 
using angka = int;

// operator komparasi berfungsi untuk membandingkan dua variabel.

int main(){

    angka a = 20;
    angka b = 50;
    cout << (a == b) <<'\n';
    
    angka c = 20;
    angka d = 50;
    cout << (c < d) <<'\n';
    
    angka e = 20;
    angka f = 50;
    cout << (e > f) <<'\n';
    
    angka g = 20;
    angka h = 50;
    cout << (g <= h) <<'\n';
    
    angka i = 20;
    angka j = 50;
    cout << (i >= j) <<'\n';
    
    angka k = 20;
    angka l = 50;
    cout << (k != l) <<'\n';

    return 0;





}